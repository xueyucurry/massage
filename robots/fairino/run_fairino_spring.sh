  echo "未找到脚本: $SCRIPT_PATH" >&2
  exit 1
fi

while [[ $# -gt 0 ]]; do
  case "$1" in
    -h|--help)
      usage
      exit 0
      ;;
    --config)
      if [[ $# -lt 2 ]]; then
        echo "--config 需要一个文件路径" >&2
        exit 1
      fi
      CONFIG_FILE="$2"
      CONFIG_FROM_CLI="1"
      shift 2
      ;;
    --print-config)
      PRINT_CONFIG="1"
      shift
      ;;
    --dry-run)
      DRY_RUN="1"
      shift
      ;;
    *)
      PASSTHROUGH_ARGS+=("$1")
      shift
      ;;
  esac
done

if [[ "$CONFIG_FROM_CLI" == "1" && ! -f "$CONFIG_FILE" ]]; then
  echo "指定的配置文件不存在: $CONFIG_FILE" >&2
  exit 1
fi

if [[ -n "${FAIRINO_SPRING_ENV:-}" && ! -f "$CONFIG_FILE" ]]; then
  echo "FAIRINO_SPRING_ENV 指向的配置文件不存在: $CONFIG_FILE" >&2
  exit 1
fi

if [[ -f "$CONFIG_FILE" ]]; then
  # shellcheck disable=SC1090
  source "$CONFIG_FILE"
fi

ROBOT_IP="${ROBOT_IP:-$DEFAULT_ROBOT_IP}"
MODE_AUTO="${MODE_AUTO:-$DEFAULT_MODE_AUTO}"
ROBOT_ENABLE="${ROBOT_ENABLE:-$DEFAULT_ROBOT_ENABLE}"
SLEEP_AFTER_MODE_CMD="${SLEEP_AFTER_MODE_CMD:-$DEFAULT_SLEEP_AFTER_MODE_CMD}"

SENSOR_COMPANY="${SENSOR_COMPANY:-$DEFAULT_SENSOR_COMPANY}"
SENSOR_DEVICE="${SENSOR_DEVICE:-$DEFAULT_SENSOR_DEVICE}"

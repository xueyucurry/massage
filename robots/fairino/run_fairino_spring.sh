usage() {
  cat <<'USAGE_EOF'
用法:
  ./run_fairino_spring.sh
  ./run_fairino_spring.sh --config /path/to/site_a.env
  ./run_fairino_spring.sh --print-config
  ./run_fairino_spring.sh --dry-run

说明:
  - 这是“原位弹簧模拟 / 托举顺应”启动器
  - 默认读取 /home/franka/massage/robots/fairino/fairino_spring.env
  - 推荐先用 --print-config 和 --dry-run 检查
USAGE_EOF
}

choose_python() {
  if [[ -x "$PROJECT_DIR/.venv/bin/python" ]]; then
    echo "$PROJECT_DIR/.venv/bin/python"
    return 0
  fi
  if [[ -x "/home/franka/anaconda3/envs/llamauav/bin/python" ]]; then
    echo "/home/franka/anaconda3/envs/llamauav/bin/python"
    return 0
  fi
  if command -v python3 >/dev/null 2>&1; then
    command -v python3
    return 0
  fi
  return 1
}

is_truthy() {
  case "${1:-0}" in
    1|true|TRUE|yes|YES|on|ON|y|Y)
      return 0
      ;;
    *)
      return 1
      ;;
  esac
}

append_if_nonempty() {
  local -n _cmd_ref="$1"
  local flag="$2"
  local value="$3"
  if [[ -n "$value" ]]; then
    _cmd_ref+=("$flag" "$value")
  fi
}


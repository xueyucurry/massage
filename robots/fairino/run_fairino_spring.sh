RETURN_RELEASE_SPEED="${RETURN_RELEASE_SPEED:-$DEFAULT_RETURN_RELEASE_SPEED}"
RETURN_CMDT="${RETURN_CMDT:-$DEFAULT_RETURN_CMDT}"
RETURN_CMD_TYPE="${RETURN_CMD_TYPE:-$DEFAULT_RETURN_CMD_TYPE}"

HOLD_SECONDS="${HOLD_SECONDS:-$DEFAULT_HOLD_SECONDS}"
LOG_INTERVAL="${LOG_INTERVAL:-$DEFAULT_LOG_INTERVAL}"

if [[ "$PRINT_CONFIG" == "1" ]]; then
  print_effective_config
  exit 0
fi

if ! PYTHON_BIN="$(choose_python)"; then
  echo "未找到可用的 Python 解释器" >&2
  exit 1
fi

cd "$PROJECT_DIR"

cmd=(
  "$PYTHON_BIN"
  "$SCRIPT_PATH"
  --ip "$ROBOT_IP"
  --sleep-after-mode-cmd "$SLEEP_AFTER_MODE_CMD"
  --sensor-company "$SENSOR_COMPANY"
  --sensor-device "$SENSOR_DEVICE"
  --sensor-softversion "$SENSOR_SOFTVERSION"
  --sensor-bus "$SENSOR_BUS"
  --sleep-after-sensor-cmd "$SLEEP_AFTER_SENSOR_CMD"
  --control-mode "$CONTROL_MODE"
  --workspace "$WORKSPACE"
  --force-threshold "$FORCE_THRESHOLD"
  --mass "$MASS"
  --damping "$DAMPING"
  --stiffness "$STIFFNESS"
  --max-v "$MAX_V"
  --max-va "$MAX_VA"
  --max-w "$MAX_W"
  --max-wa "$MAX_WA"
  --drag-backend "$DRAG_BACKEND"
  --drag-lambda "$DRAG_LAMBDA"
  --torque-spring-k "$TORQUE_SPRING_K"
  --torque-spring-d "$TORQUE_SPRING_D"
  --torque-spring-max-delta "$TORQUE_SPRING_MAX_DELTA"
  --torque-spring-deadband "$TORQUE_SPRING_DEADBAND"
  --torque-loop-interval "$TORQUE_LOOP_INTERVAL"
  --torque-cmd-type "$TORQUE_CMD_TYPE"
  --torque-power-limit "$TORQUE_POWER_LIMIT"
  --drag-impedance-flag "$DRAG_IMPEDANCE_FLAG"
  --drag-k "$DRAG_K"
  --drag-b "$DRAG_B"
  --drag-max-tcp-vel "$DRAG_MAX_TCP_VEL"
  --drag-max-tcp-ori-vel "$DRAG_MAX_TCP_ORI_VEL"
  --drag-exit-timeout "$DRAG_EXIT_TIMEOUT"
  --drag-exit-poll-interval "$DRAG_EXIT_POLL_INTERVAL"
  --sleep-after-drag-exit "$SLEEP_AFTER_DRAG_EXIT"
  --return-enabled "$RETURN_ENABLED"
  --return-force-threshold "$RETURN_FORCE_THRESHOLD"
  --return-reacquire-force "$RETURN_REACQUIRE_FORCE"
  --return-reacquire-delay "$RETURN_REACQUIRE_DELAY"
  --return-reacquire-speed "$RETURN_REACQUIRE_SPEED"
  --return-release-hold "$RETURN_RELEASE_HOLD"
  --return-translation-band "$RETURN_TRANSLATION_BAND"
  --return-rotation-band "$RETURN_ROTATION_BAND"
  --return-k-xyz "$RETURN_K_XYZ"
  --return-k-rpy "$RETURN_K_RPY"
  --return-max-step-xyz "$RETURN_MAX_STEP_XYZ"
  --return-max-step-rpy "$RETURN_MAX_STEP_RPY"
  --return-joint-k "$RETURN_JOINT_K"
  --return-joint-max-step "$RETURN_JOINT_MAX_STEP"
  --return-joint-band "$RETURN_JOINT_BAND"
  --return-release-speed "$RETURN_RELEASE_SPEED"
  --return-cmdt "$RETURN_CMDT"
  --return-cmd-type "$RETURN_CMD_TYPE"
  --hold-seconds "$HOLD_SECONDS"
  --log-interval "$LOG_INTERVAL"
)

append_if_nonempty cmd --payload-weight "$PAYLOAD_WEIGHT"
append_if_nonempty cmd --payload-cog "$PAYLOAD_COG"

if is_truthy "$MODE_AUTO"; then
  cmd+=(--mode-auto)
fi

if is_truthy "$ROBOT_ENABLE"; then
  cmd+=(--enable)
fi

if is_truthy "$SKIP_ZERO"; then
  cmd+=(--skip-zero)
fi

if [[ "${#PASSTHROUGH_ARGS[@]}" -gt 0 ]]; then
  cmd+=("${PASSTHROUGH_ARGS[@]}")
fi

if [[ "$DRY_RUN" == "1" ]]; then
  print_effective_config
  printf '[DryRun] '
  printf '%q ' "${cmd[@]}"
  printf '\n'
  exit 0
fi

exec "${cmd[@]}"

SENSOR_BUS="${SENSOR_BUS:-$DEFAULT_SENSOR_BUS}"
SKIP_ZERO="${SKIP_ZERO:-$DEFAULT_SKIP_ZERO}"
SLEEP_AFTER_SENSOR_CMD="${SLEEP_AFTER_SENSOR_CMD:-$DEFAULT_SLEEP_AFTER_SENSOR_CMD}"
PAYLOAD_WEIGHT="${PAYLOAD_WEIGHT:-$DEFAULT_PAYLOAD_WEIGHT}"
PAYLOAD_COG="${PAYLOAD_COG:-$DEFAULT_PAYLOAD_COG}"

CONTROL_MODE="${CONTROL_MODE:-$DEFAULT_CONTROL_MODE}"
WORKSPACE="${WORKSPACE:-$DEFAULT_WORKSPACE}"
FORCE_THRESHOLD="${FORCE_THRESHOLD:-$DEFAULT_FORCE_THRESHOLD}"
MASS="${MASS:-$DEFAULT_MASS}"
DAMPING="${DAMPING:-$DEFAULT_DAMPING}"
STIFFNESS="${STIFFNESS:-$DEFAULT_STIFFNESS}"
MAX_V="${MAX_V:-$DEFAULT_MAX_V}"
MAX_VA="${MAX_VA:-$DEFAULT_MAX_VA}"
MAX_W="${MAX_W:-$DEFAULT_MAX_W}"
MAX_WA="${MAX_WA:-$DEFAULT_MAX_WA}"

DRAG_BACKEND="${DRAG_BACKEND:-$DEFAULT_DRAG_BACKEND}"
DRAG_LAMBDA="${DRAG_LAMBDA:-$DEFAULT_DRAG_LAMBDA}"
TORQUE_SPRING_K="${TORQUE_SPRING_K:-$DEFAULT_TORQUE_SPRING_K}"
TORQUE_SPRING_D="${TORQUE_SPRING_D:-$DEFAULT_TORQUE_SPRING_D}"
TORQUE_SPRING_MAX_DELTA="${TORQUE_SPRING_MAX_DELTA:-$DEFAULT_TORQUE_SPRING_MAX_DELTA}"
TORQUE_SPRING_DEADBAND="${TORQUE_SPRING_DEADBAND:-$DEFAULT_TORQUE_SPRING_DEADBAND}"
TORQUE_LOOP_INTERVAL="${TORQUE_LOOP_INTERVAL:-$DEFAULT_TORQUE_LOOP_INTERVAL}"
TORQUE_CMD_TYPE="${TORQUE_CMD_TYPE:-$DEFAULT_TORQUE_CMD_TYPE}"
TORQUE_POWER_LIMIT="${TORQUE_POWER_LIMIT:-$DEFAULT_TORQUE_POWER_LIMIT}"
DRAG_IMPEDANCE_FLAG="${DRAG_IMPEDANCE_FLAG:-$DEFAULT_DRAG_IMPEDANCE_FLAG}"
DRAG_K="${DRAG_K:-$DEFAULT_DRAG_K}"
DRAG_B="${DRAG_B:-$DEFAULT_DRAG_B}"
DRAG_MAX_TCP_VEL="${DRAG_MAX_TCP_VEL:-$DEFAULT_DRAG_MAX_TCP_VEL}"

DRAG_EXIT_POLL_INTERVAL="${DRAG_EXIT_POLL_INTERVAL:-$DEFAULT_DRAG_EXIT_POLL_INTERVAL}"
SLEEP_AFTER_DRAG_EXIT="${SLEEP_AFTER_DRAG_EXIT:-$DEFAULT_SLEEP_AFTER_DRAG_EXIT}"

